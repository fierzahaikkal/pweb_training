//deklarasi variabel menggunakan let
let x = 4;
let y = 5;
let tambah = x + y; //menggunakan operator aritmatika pertambahan
let bagi = x / y; //menggunakan operator aritmatika pembagian
let modulo = x % y; //menggunakan operator aritmatika modulo

tambah += 4; //sama seperti tambah = tambah + 4
bagi /= 2; //sama seperti bagi = bagi / 2
modulo %= 2; //sama seperti modulo = modulo % 2

console.log("hasil = " + tambah); //hasil = 13
console.log("hasil = " + bagi); //hasil = 0.4
console.log("hasil = " + modulo); //hasil = 0
