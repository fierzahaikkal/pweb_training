// deklarasi variabel dengan let
let x = 4;
let y = 5;
let andOperator = x < y && x <= 4; //operator logika didalam sebuah variabel
let orOperator = x > y || x < 4;
let negasiOperator = !(x > y);

console.log("hasil AND = " + andOperator); //true
console.log("hasil OR = " + orOperator); // false
console.log("hasil Negasi = " + negasiOperator); //true
